#include <stdio.h>
int main()
{
    //working of arithmetic operators
    int a = 9,b = 4, c;
    //sum of two numbers
    c= a+b;
    printf("sum is : %d\n" , c);

    //minus of given number
    c= a-b;
    printf("minus is : %d\n",c);

    //multiplication 
    c= a*b;
    printf("multiplication is : %d\n", c);

    //division 
    c= a/b;
    printf("division is : %d\n", c);

    //reminder
    c= a%b;
    printf("reminder is : %d\n", c);
  
    return 0;

}
